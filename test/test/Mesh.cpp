Skull::Skull(ID3D12Device* pd3dDevice, ID3D12GraphicsCommandList* pd3dCommandList, float fWidth, float fHeight, float fDepth) : CMesh(pd3dDevice, pd3dCommandList)
{
	std::ifstream fin("skull.txt");
	if (!fin)
	{
		MessageBox(0, L"Models/skull.txt not found.", 0, 0);
		return;
	}

	std::string ignore;

	fin >> ignore >> ignore;
	fin >> ignore >> ignore;
	fin >> ignore >> ignore >> ignore >> ignore;

	m_nVertices = 31076;
	m_nStride = sizeof(CDiffusedVertex);
	m_nOffset = 0;
	m_nSlot = 0;
	m_d3dPrimitiveTopology = D3D_PRIMITIVE_TOPOLOGY_TRIANGLELIST;

	auto pVertices = new CDiffusedVertex[m_nVertices];
	for (UINT i = 0; i < m_nVertices; ++i) {
		float a, b, c;
		fin >> a >> b >> c;
		fin >> ignore >> ignore >> ignore;
		XMFLOAT3 tmp;
		XMStoreFloat3(&tmp, XMVector3TransformCoord(XMVECTOR{ a * fWidth,b * fHeight,c * fDepth }, XMMatrixRotationY(3.141592)));
		if (c <0 && c>-1 && b < 5 && b>3 && a<2 && a>-2) pVertices[i] = CDiffusedVertex(tmp, XMFLOAT4(0, 0, 0, 1));
		//	else if(i<3000) pVertices[i] = CDiffusedVertex(XMFLOAT3{ a * fWidth,b * fHeight,c * fDepth }, XMFLOAT4(0, 0, 0, 1));
		else// pVertices[i] = CDiffusedVertex(XMFLOAT3{ a * fWidth,b * fHeight,c * fDepth }, XMFLOAT4(1,1, 1, 1));
			pVertices[i] = CDiffusedVertex(tmp,
				XMFLOAT4(
					static_cast<float>(m_nVertices - i) / m_nVertices, 
					static_cast<float>(m_nVertices - i) / m_nVertices, 
					static_cast<float>(m_nVertices - i) / m_nVertices, 1));
	}

	m_pd3dVertexBuffer = CreateBufferResource(pd3dDevice, pd3dCommandList, pVertices, m_nStride * m_nVertices, D3D12_HEAP_TYPE_DEFAULT, D3D12_RESOURCE_STATE_VERTEX_AND_CONSTANT_BUFFER, &m_pd3dVertexUploadBuffer);

	m_d3dVertexBufferView.BufferLocation = m_pd3dVertexBuffer->GetGPUVirtualAddress();
	m_d3dVertexBufferView.StrideInBytes = m_nStride;
	m_d3dVertexBufferView.SizeInBytes = m_nStride * m_nVertices;

	fin >> ignore;
	fin >> ignore;
	fin >> ignore;

	m_nIndices = 3 * 60339;

	auto pnIndices = new UINT[m_nIndices];
	for (UINT i = 0; i < m_nIndices; ++i)
	{
		fin >> pnIndices[i * 3 + 0] >> pnIndices[i * 3 + 1] >> pnIndices[i * 3 + 2];
	}
	fin.close();

	m_pd3dIndexBuffer = CreateBufferResource(pd3dDevice, pd3dCommandList, pnIndices, sizeof(UINT) * m_nIndices, D3D12_HEAP_TYPE_DEFAULT, D3D12_RESOURCE_STATE_INDEX_BUFFER, &m_pd3dIndexUploadBuffer);

	m_d3dIndexBufferView.BufferLocation = m_pd3dIndexBuffer->GetGPUVirtualAddress();
	m_d3dIndexBufferView.Format = DXGI_FORMAT_R32_UINT;
	m_d3dIndexBufferView.SizeInBytes = sizeof(UINT) * m_nIndices;
}

